# Data Types 

# 1. String
name = "Riazul"
print(name)
print(type(name))

# 2. Integer
age = 20
print(age)
print(type(age))

# 3. Float
pi = 3.14
print(pi)
print(type(pi))

# 4. Boolean
is_student = True
print(is_student)
print(type(is_student))

# 5. List
fruits = ["apple", "banana", "orange"]
print(fruits)
print(type(fruits))

# 6. Tuple
coordinates = (10, 20)
print(coordinates)
print(type(coordinates))

# 7. Set
colors = {"red", "green", "blue"}
print(colors)
print(type(colors))

# 8. Dictionary
person = {"name": "Riazul", "age": 20}
print(person)
print(type(person))

# 9. None
value = None
print(value)
print(type(value))

# 10. Complex
complex_number = 3 + 4j
print(complex_number)
print(type(complex_number))

# 11. Range
numbers = range(5)
print(numbers)
print(type(numbers))

# 12. Byte
byte_value = b'Hello'
print(byte_value)
print(type(byte_value))

# 13. Buffer
buffer_value = memoryview(b'Hello')
print(buffer_value)
print(type(buffer_value))

# 14. Ellipsis
ellipsis_value = ...
print(ellipsis_value)
print(type(ellipsis_value))

# 15. Frozen Set
frozen_set = frozenset({"apple", "banana", "orange"})
print(frozen_set)
print(type(frozen_set))

# 16. Bytearray
bytearray_value = bytearray(b'Hello')
print(bytearray_value)
print(type(bytearray_value))

# 17. Memoryview
memoryview_value = memoryview(b'Hello')
print(memoryview_value)
print(type(memoryview_value))