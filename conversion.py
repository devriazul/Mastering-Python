# explicit coversion

a = 10
b = 3.14
c = int(b)
print(c)

# Output:
# 3


# float to int
a = 10
b = float(a)
print(b)

# Output:
# 10.0


# int to float
a = 10
b = float(a)
print(b)

# Output:
# 10.0


#  implicit conversion

a = 10
b = 3.14
c = a + b
print(c)

# Output:
# 13.14


# int to str
a = 10
b = str(a)
print(b)

# Output:
# 10


# str to int
a = "10"
b = int(a)
print(b)

# Output:
# 10


# str to float
a = "3.14"
b = float(a)
print(b)

# Output:
# 3.14